// Format price to VND
export function formatPrice(price) {
  return new Intl.NumberFormat('vi-VN').format(price) + ' đ';
}

// Search components with fuzzy matching
export function searchComponents(query, categoryFilter = 'all') {
  const q = query.toLowerCase().trim();
  let results = COMPONENTS;

  if (categoryFilter !== 'all') {
    results = results.filter(c => c.category === categoryFilter);
  }

  if (!q) return results;

  return results.filter(c => {
    const searchableText = [
      c.name,
      c.manufacturer,
      c.package,
      c.description,
      c.specs.voltage,
      c.specs.current,
      c.specs.interface,
    ].join(' ').toLowerCase();

    // Split query into words for fuzzy matching
    const words = q.split(/\s+/);
    return words.every(word => searchableText.includes(word));
  }).sort((a, b) => {
    // Prioritize exact name matches
    const aNameMatch = a.name.toLowerCase().includes(q);
    const bNameMatch = b.name.toLowerCase().includes(q);
    if (aNameMatch && !bNameMatch) return -1;
    if (!aNameMatch && bNameMatch) return 1;
    return 0;
  });
}
